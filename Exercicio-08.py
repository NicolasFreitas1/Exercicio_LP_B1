a = input("Digite algo: ")

print("É um numero numerico? " ,a.isnumeric())
print("É um decimal? ", a.isdecimal())
print("É uma letra do alfabeto? ", a.isalpha())
print("Esta em letra minuscula? ", a.islower())
print("Está em caixa alta? ", a.isupper())
print("A primeira letra é uma maiuscula? ", a.istitle())
